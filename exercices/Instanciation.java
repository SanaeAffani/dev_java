public class Instanciation {

    public static void main(String[] args) {
        // Définir une variable de type Object
        Object monObjet;

        // Définition et instanciation d'un nouvel objet dans une nouvelle variable
        Object instanceMonObjet = new Object();

        // Affectation de la valeur null à cet objet
        instanceMonObjet = null;
        System.out.println("la valeur de mon objet est :" + instanceMonObjet);

    }

}
