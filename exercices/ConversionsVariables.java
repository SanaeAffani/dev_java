public class ConversionsVariables {

    public static void main(String[] args) {

        // Conversion d'un short en int

        short varShort = 56;

        int intConvertiEnShort = (int) varShort;

        System.out.println("Conversion d'un short en int = " + intConvertiEnShort);

        // Conversion d'un int en short

        int varInt = 25;

        short shortConvertiEnInt = (short) varInt;
        System.out.println("Conversion d'un int en short = " + shortConvertiEnInt);
    }

}